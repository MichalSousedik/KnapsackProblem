public class Pair {
    private int pos;
    private double value;

    public Pair(int pos, double value) {
        this.pos = pos;
        this.value = value;
    }

    public int getPos() {
        return pos;
    }

    public void setPos(int pos) {
        this.pos = pos;
    }

    public double getValue() {
        return value;
    }

    public void setValue(double value) {
        this.value = value;
    }
}

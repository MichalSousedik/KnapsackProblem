public class Pair {
    private int pos;
    private double frac;

    public Pair(int pos, double frac) {
        this.pos = pos;
        this.frac = frac;
    }

    public int getPos() {
        return pos;
    }

    public void setPos(int pos) {
        this.pos = pos;
    }

    public double getFrac() {
        return frac;
    }

    public void setFrac(double frac) {
        this.frac = frac;
    }
}
